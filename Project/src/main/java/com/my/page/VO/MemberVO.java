package com.my.page.VO;

public class MemberVO {
	private String mbIdx;
	private String mbID;
	private String mbPW;
	private String mbNM;
	private String mbEmail;
	
	public String getMbIdx() {
		return mbIdx;
	}
	public void setMbIdx(String mbIdx) {
		this.mbIdx = mbIdx;
	}
	public String getMbID() {
		return mbID;
	}
	public void setMbID(String mbID) {
		this.mbID = mbID;
	}
	public String getMbPW() {
		return mbPW;
	}
	public void setMbPW(String mbPW) {
		this.mbPW = mbPW;
	}
	public String getMbNM() {
		return mbNM;
	}
	public void setMbNM(String mbNM) {
		this.mbNM = mbNM;
	}
	public String getMbPhone() {
		return mbPhone;
	}
	public void setMbPhone(String mbPhone) {
		this.mbPhone = mbPhone;
	}
	public String getMbEmail() {
		return mbEmail;
	}
	public void setMbEmail(String mbEmail) {
		this.mbEmail = mbEmail;
	}
	private String mbPhone;
}
